# A List is a collection which is ordered and changeable. Allows duplicate members.
# itss like an array in js 

# create a list 
numbers =[1,2,3]
print(numbers)


fruits=['apple' , "bananas" , "oranges"]
# get a value 
print(fruits[1])

#  get length 
print(len(fruits))

# append
fruits.append('mangoes')

# remove 
fruits.remove('apple')

# insert 
fruits.insert(2, 'grapes')

# remove with pop 
fruits.pop(2)

# reverse the list 
fruits.reverse()

# sort 
fruits.sort()

# reverse sort 
fruits.reverse()

# change a value 
fruits[0]= "blueberry"


print(fruits)




